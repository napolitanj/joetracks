export function getConditionFromPercentile(
  currentCFS: number | null,
  thresholds: PercentileThresholds | null,
): RiverCondition {
  if (currentCFS === null || thresholds === null) return "unknown";
  if (currentCFS > thresholds.p90) return "high";
  if (currentCFS >= thresholds.p75) return "above_normal";
  if (currentCFS >= thresholds.p25) return "normal";
  if (currentCFS >= thresholds.p10) return "below_normal";
  return "low";
}
